let a = true; // Assignment

let m = 20;
let n = 30;

// +,-,*,/,%
console.log(m + n); // Arithmetic

// Comparison
// >, <, >=, <= , ==, ===, !=
console.log(m > n);

// Logical
// &&, ||, !
console.log(m > n && a);

// String concat
let z = "Hello";
let y = "World";

console.log(z + y);

// Ternary
m > n ? console.log("m is greater than n") : console.log("m is less than n");
